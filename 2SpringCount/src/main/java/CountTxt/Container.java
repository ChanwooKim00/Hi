package CountTxt;

public class Container {
	InDummy In;
	OutDummy Out;
	CountDummy Count;
	
	public InDummy getIn() {
		return In;
	}
	public void setIn(InDummy in) {
		In = in;
	}
	public OutDummy getOut() {
		return Out;
	}
	public void setOut(OutDummy out) {
		Out = out;
	}
	public CountDummy getCount() {
		return Count;
	}
	public void setCount(CountDummy count) {
		Count = count;
	}

}
